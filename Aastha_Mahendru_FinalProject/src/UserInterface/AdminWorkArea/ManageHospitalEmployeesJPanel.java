    private void addEmployeeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addEmployeeJButtonActionPerformed
        try {
            String firstName = fnameJTextField.getText();
            String lastName = lnameJTextField.getText();
            String address = addressJTextField.getText();
            long contact = Long.parseLong(contactJTextField.getText());
            HospitalOrganization horg = (HospitalOrganization) hospitalJComboBox2.getSelectedItem();

            CareTeamLeader emp = new CareTeamLeader();
            emp.setFirstName(firstName);
            emp.setLastName(lastName);
            emp.setAddress(address);
            emp.setContactNo(contact);
            emp.setHospital(horg);
            horg.getEmployeeDirectory().getEmployeeList().add(emp);
            populateTable();
            JOptionPane.showMessageDialog(null, "Care Team Leader has been added.");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please Enter Valid Information.");
            return;
        }
    }//GEN-LAST:event_addEmployeeJButtonActionPerformed

