    private void removeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJButtonActionPerformed
        int selectedRow = (int) employeesJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row.");
            return;
        }
        int result = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this employee?", "Warning", JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_OPTION) {
            CareTeamLeader emp = (CareTeamLeader) employeesJTable.getValueAt(selectedRow, 1);
            HospitalOrganization horg = (HospitalOrganization) hospitalJComboBox1.getSelectedItem();
            horg.getEmployeeDirectory().removeEmployee(emp);
            populateTable();
        }
    }//GEN-LAST:event_removeJButtonActionPerformed


