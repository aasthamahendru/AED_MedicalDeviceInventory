    public void populateEntComboBox() {
        entJComboBox.removeAllItems();
        for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseList()) {
            entJComboBox.addItem(e);
        }

    }

    public void populateOrganizationComboBox(Enterprise ent) {
        if (ent != null) {
            if (ent.getEnterpriseType().equals(Enterprise.EnterpriseType.Hospital)) {
                populateHospitalOrgComboBox(ent);
            }
            if (ent.getEnterpriseType().equals(Enterprise.EnterpriseType.Supplier)) {
                populateSupplierOrgComboBox(ent);
            }
            if (ent.getEnterpriseType().equals(Enterprise.EnterpriseType.Inventory)) {
                populateInventoryOrgComboBox(ent);
            }
        }

    }

    public void populateHospitalOrgComboBox(Enterprise e) {
        organizationJComboBox.removeAllItems();

        for (Organization org : e.getOrganizationDirectory().getOrganizationList()) {
            HospitalOrganization horg = (HospitalOrganization) org;
            organizationJComboBox.addItem(horg);
        }
    }

    public void populateSupplierOrgComboBox(Enterprise e) {
        organizationJComboBox.removeAllItems();

        for (Organization org : e.getOrganizationDirectory().getOrganizationList()) {
            SupplierOrganization sorg = (SupplierOrganization) org;
            organizationJComboBox.addItem(sorg);
        }
    }

    public void populateInventoryOrgComboBox(Enterprise e) {
        organizationJComboBox.removeAllItems();

        for (Organization org : e.getOrganizationDirectory().getOrganizationList()) {
            if (org instanceof InventoryOrganization) {
                InventoryOrganization iorg = (InventoryOrganization) org;
                organizationJComboBox.addItem(org);
            } else if (org instanceof MaintenanceOrganization) {
                MaintenanceOrganization morg = (MaintenanceOrganization) org;
                organizationJComboBox.addItem(morg);
            }
        }
    }

    public void populateRoleComboBox(Organization org) {
        for (Role role : org.getSupportedRole()) {
            roleJComboBox.addItem(role);
        }
    }

