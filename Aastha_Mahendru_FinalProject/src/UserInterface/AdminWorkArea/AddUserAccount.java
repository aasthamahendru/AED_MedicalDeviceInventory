    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

        try {
            String userName = usernameJTextField.getText();
            String password = String.valueOf(passwordJTextField.getPassword());
            boolean check = false;
            if (userName.equals("") || password.equals("")) {
                JOptionPane.showMessageDialog(null, "Please enter all details.");
                return;

            }

            for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (Organization org : e.getOrganizationDirectory().getOrganizationList()) {
                    check = org.getUserAccountDirectory().checkIfUsernameIsUnique(userName);
                }
            }

            if (check == true) {
                Employee emp = (Employee) employeeJComboBox.getSelectedItem();
                Organization org = (Organization) organizationJComboBox.getSelectedItem();
                Role role = (Role) roleJComboBox.getSelectedItem();
                if (org != null) {
                    if (!activeJRadioButton.isSelected()) {
                        org.getUserAccountDirectory().createUserAccount(userName, password, emp, role, network, "Disable");

                    } else {
                        org.getUserAccountDirectory().createUserAccount(userName, password, emp, role, network, "Active");

                    }

                }
            }

            JOptionPane.showMessageDialog(null, "User Account added.");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please Enter Valid Information.");
            return;
        }

    }//GEN-LAST:event_addJButtonActionPerformed

