    private void viewSupplierJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewSupplierJButtonActionPerformed
        int selectedRow = suppliersJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row.");
            return;
        }
        SupplierOrganization sorg = (SupplierOrganization) suppliersJTable.getValueAt(selectedRow, 1);

        ViewSJPanel vjp = new ViewSJPanel(upc, sorg.getS());
        upc.add("ViewSupplier", vjp);
        CardLayout layout = (CardLayout) upc.getLayout();
        layout.next(upc);

    }//GEN-LAST:event_viewSupplierJButtonActionPerformed

    private void removeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJButtonActionPerformed

        int selectedRow = (int) suppliersJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row.");
            return;
        }
        int result = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this supplier?", "Warning", JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_OPTION) {
            SupplierOrganization s = (SupplierOrganization) suppliersJTable.getValueAt(selectedRow, 1);
            supplierEnt.getOrganizationDirectory().getOrganizationList().remove(s);
            supplierEnt.getEmployeeDirectory().getEmployeeList().remove(s.getS());
        }
        populateTable();
    }//GEN-LAST:event_removeJButtonActionPerformed

