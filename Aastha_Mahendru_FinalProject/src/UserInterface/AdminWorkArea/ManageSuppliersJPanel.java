    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) suppliersJTable.getModel();
        dtm.setRowCount(0);

        Object[] row = new Object[2];
        for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseList()) {
            if (e instanceof SupplierEnterprise) {
                supplierEnt = (SupplierEnterprise) e;
                for (Organization horg : e.getOrganizationDirectory().getOrganizationList()) {
                    SupplierOrganization temphorg = (SupplierOrganization) horg;
                    row[0] = temphorg.getOrganizationID();
                    row[1] = temphorg;

                    dtm.addRow(row);
                }
            }
        }

    }

