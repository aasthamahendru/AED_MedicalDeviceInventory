    public void populateOrderHistoryTable() {
        DefaultTableModel dtm = (DefaultTableModel) orderHistoryJTable.getModel();
        dtm.setRowCount(0);
        Object[] row = new Object[3];
        for (Order o : moc.getMoc()) {
            row[0] = o;
            row[1] = o.getSupplier();
            row[2] = o.getTotalamount();

            dtm.addRow(row);
        }
    }

