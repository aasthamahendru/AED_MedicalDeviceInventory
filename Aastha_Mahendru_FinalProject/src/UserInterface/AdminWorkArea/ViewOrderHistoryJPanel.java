    private void viewOrderBillJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewOrderBillJButtonActionPerformed
        int selectedRow = orderHistoryJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row.");
            return;
        }
        Order o = (Order) orderHistoryJTable.getValueAt(selectedRow, 0);
        ViewOrderBillJPanel vodjp = new ViewOrderBillJPanel(upc, o);
        upc.add("ViewOrderItemJPanel", vodjp);
        CardLayout layout = (CardLayout) upc.getLayout();
        layout.next(upc);
    }//GEN-LAST:event_viewOrderBillJButtonActionPerformed

