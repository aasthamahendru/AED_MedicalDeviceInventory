    public void populateWarehouseTable() {
        DefaultTableModel dtm = (DefaultTableModel) warehouseJTable.getModel();
        dtm.setRowCount(0);
        for (Organization org : inventoryEnterprise.getOrganizationDirectory().getOrganizationList()) {
            if (org instanceof InventoryOrganization) {
                iorg = (InventoryOrganization) org;
                break;
            }
        }
        Object[] row = new Object[3];
        wd = iorg.getWarehouseDirectory();
        for (Warehouse w : wd.getWarehouseList()) {
            row[0] = w;
            row[1] = w.getAddress();
            row[2] = w.getId();

            dtm.addRow(row);
        }
    }

