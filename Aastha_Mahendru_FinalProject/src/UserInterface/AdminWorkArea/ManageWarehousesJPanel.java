    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed
        String name = newWarehouseNameJTextField.getText();
        String address = addressJTextField.getText();
        if (!name.equals("") || !name.equals("")) {
            wd.addWarehouse(name, address);
            populateWarehouseTable();
            JOptionPane.showMessageDialog(null, "Warehouse added successfully.");
        } else {
            JOptionPane.showMessageDialog(null, "Please enter valid and complete information.");
            return;
        }
    }//GEN-LAST:event_addJButtonActionPerformed

    private void manageWarehouseJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_manageWarehouseJButtonActionPerformed
        int selectedRow = warehouseJTable.getSelectedRow();
       
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row.");
            return;
        }
        Warehouse warehouse = (Warehouse) warehouseJTable.getValueAt(selectedRow, 0);
        ManageWarehouseEmployeesJPanel mwe = new ManageWarehouseEmployeesJPanel(upc, inventoryEnterprise, warehouse);
        upc.add("managewarehouseemployees", mwe);
        CardLayout layout = (CardLayout) upc.getLayout();
        layout.next(upc);
    }//GEN-LAST:event_manageWarehouseJButtonActionPerformed

    private void removeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeJButtonActionPerformed
        int selectedRow = warehouseJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row.");
            return;
        }
        int result = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this warehouse?", "Warning", JOptionPane.YES_NO_OPTION);
        if (result == JOptionPane.YES_OPTION) {
            Warehouse warehouse = (Warehouse) warehouseJTable.getValueAt(selectedRow, 0);
            wd.removeWarehouse(warehouse);
            populateWarehouseTable();
        }
    }//GEN-LAST:event_removeJButtonActionPerformed

